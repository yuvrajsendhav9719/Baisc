package basic414;

public class loopswitch {
	
public static void main(String[]args) {
		
String month ="june";

switch (month) {

case "may":
System.out.println("may it's my birthday month");
break;

case"june":
	System.out.println("june it's my birthday month");
break;

case"july":
	System.out.println("july it's my birthday month");
break;

default:
    System.out.println("it's not my birthday month");	
}
	}

}
